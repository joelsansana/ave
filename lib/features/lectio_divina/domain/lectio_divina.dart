class LectioDivina {
  final String id;
  final String title;
  final String reference;
  final String introduction;
  final String lectioText;
  final String lectioQuestion;
  final String meditatioText;
  final String meditatioQuestion;
  final String oratioText;
  final String oratioPrompt;
  final String contemplatioText;
  final String contemplatioPrompt;

  const LectioDivina({
    required this.id,
    required this.title,
    required this.reference,
    required this.introduction,
    required this.lectioText,
    required this.lectioQuestion,
    required this.meditatioText,
    required this.meditatioQuestion,
    required this.oratioText,
    required this.oratioPrompt,
    required this.contemplatioText,
    required this.contemplatioPrompt,
  });
}
